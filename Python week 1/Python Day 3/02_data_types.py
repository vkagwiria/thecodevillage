# Examples of Data Types:
# Integer - These are whole numbers e.g 5
number1 = 5
print(number1)

# Float - These are decimal numbers e.g 5.7
number2 = 5.7
print(number2)

# Boolean - These are True or False values e.g True
switch = True
print(switch)

# String - These are characters within quotes e.g "Hello"
greeting = "Hello"
print(greeting)

